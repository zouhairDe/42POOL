int	ft_sqrt(int nb)
{
	int	rad;

	rad = 1;
	if (nb > 0)
	{
		while (rad * rad <= nb && rad <= 46340)
		{
			if (rad * rad == nb)
				return (rad);
			rad++;
		}
	}
	return (0);
}
