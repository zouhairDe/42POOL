int	ft_atoi(char *str)
{
	int	sign;
	int	nb;

	sign = 1;
	nb = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = sign * -1;
		str++;
	}
	while (*str <= '9' && *str >= '0')
	{
		nb = (10 * nb) + (*str - 48) * sign;
		str++;
	}
	return (nb);
}
